import { Database } from "bun:sqlite";
import path from "path";

const DB_PATH = path.resolve(import.meta.dir, '../data/gaokao_2025.db');

// ============================================================
// 1. 建表
// ============================================================
const db = new Database(DB_PATH);

db.pragma('journal_mode = WAL');

db.exec(`
  -- 院校级录取分数线（校线）
  CREATE TABLE IF NOT EXISTS school_scores (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    school       TEXT NOT NULL,           -- 学校名称
    province     TEXT NOT NULL,           -- 招生省份（考生所在省）
    year         INTEGER NOT NULL DEFAULT 2025,
    batch        TEXT NOT NULL,           -- 批次：本科提前批/本科一批/本科批/本科二批
    subject      TEXT NOT NULL,           -- 科类：物理类/历史类/理科/文科/综合改革
    min_score    INTEGER,                 -- 最低录取分
    avg_score    INTEGER,                 -- 平均分（如有）
    max_score    INTEGER,                 -- 最高分（如有）
    min_rank     INTEGER,                 -- 最低位次
    plan_count   INTEGER,                 -- 招生计划人数
    actual_count INTEGER,                 -- 实际录取人数（如有）
    score_line   INTEGER,                 -- 当年该省该批次控制线
    source       TEXT DEFAULT '待补',     -- 数据来源
    note         TEXT,                    -- 备注
    UNIQUE(school, province, year, batch, subject)
  );

  -- 专业级录取分数线（专业线）
  CREATE TABLE IF NOT EXISTS major_scores (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    school       TEXT NOT NULL,
    province     TEXT NOT NULL,
    year         INTEGER NOT NULL DEFAULT 2025,
    batch        TEXT NOT NULL,
    subject      TEXT NOT NULL,
    major_group  TEXT,                    -- 院校专业组代码（新高考省份）
    major        TEXT NOT NULL,           -- 专业名称
    min_score    INTEGER,
    avg_score    INTEGER,
    max_score    INTEGER,
    min_rank     INTEGER,
    plan_count   INTEGER,
    actual_count INTEGER,
    source       TEXT DEFAULT '待补',
    note         TEXT,
    UNIQUE(school, province, year, batch, subject, major)
  );

  -- 各省控制线（批次线）
  CREATE TABLE IF NOT EXISTS province_lines (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    province     TEXT NOT NULL,
    year         INTEGER NOT NULL DEFAULT 2025,
    batch        TEXT NOT NULL,
    subject      TEXT NOT NULL,
    score_line   INTEGER NOT NULL,        -- 控制线分数
    source       TEXT DEFAULT '待补',
    UNIQUE(province, year, batch, subject)
  );

  -- 创建索引加速查询
  CREATE INDEX IF NOT EXISTS idx_ss_school ON school_scores(school);
  CREATE INDEX IF NOT EXISTS idx_ss_province ON school_scores(province);
  CREATE INDEX IF NOT EXISTS idx_ss_school_prov ON school_scores(school, province);
  CREATE INDEX IF NOT EXISTS idx_ms_school ON major_scores(school);
  CREATE INDEX IF NOT EXISTS idx_ms_province ON major_scores(province);
  CREATE INDEX IF NOT EXISTS idx_ms_major ON major_scores(major);
  CREATE INDEX IF NOT EXISTS idx_ms_school_prov ON major_scores(school, province);
`);

console.log('✅ 数据库表结构已创建');

